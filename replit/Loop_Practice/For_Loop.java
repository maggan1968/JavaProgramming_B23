package Loop_Practice;

import java.util.Scanner;

public class For_Loop {
    public static void main(String[] args) {
        Scanner scan=new Scanner(System.in);
        int n= scan.nextInt();

        for (int i = 1; i<=n; n++) {
            System.out.println("*");





        }

    }
}
