package day05_ArithmeticOperators;

public class DivideTwoNumbers {

    public static void main(String[] args) {
         int a = 20;
         int b = 8;
         int result = a/b;
         int remainder = a%b;

        System.out.println(a + " divide by " +b +" is equal to " +result
                +" with a remainder of " +remainder);
    }
}
