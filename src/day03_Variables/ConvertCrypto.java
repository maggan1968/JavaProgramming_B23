package day03_Variables;

public class ConvertCrypto {

    public static void main(String[] args) {
        
        double dollar = 1000;
        double bitcoin;
        bitcoin = dollar * 36494;
        double dogecoin = dollar * 0.3501;
        double tenset = dollar * 3.657;




        System.out.println("bitcoin = " + bitcoin);
        System.out.println("dogecoin = " + dogecoin);
        System.out.println("tenset = " + tenset);
    }
}
