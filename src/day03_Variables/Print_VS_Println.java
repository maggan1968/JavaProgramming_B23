package day03_Variables;

public class Print_VS_Println {

    public static void main(String[] args) {

        System.out.println("Hello Batch 23");//appends new line  after printing

        System.out.println("How are you today?");
        System.out.println("-----------------------");

        System.out.print("Hello Batch 23"); //does not append  new line
        System.out.print("How are you today?");// does not append new line


        System.out.println();
        System.out.println("---------------");
        System.out.println(" JRE stands for JAva Runtime Environment, JAVAc stand for JAva  Compiler, JDK stands for Java Development Kit");

        System.out.println("----------------------");


        System.out.print("JRE stands for JAva Runtime Environment");
        System.out.print("JAVAc stand for JAva  Compiler");
        System.out.print("JDK stands for Java Development");


    }


}
