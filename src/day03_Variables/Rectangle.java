package day03_Variables;

public class Rectangle {


    public static void main(String[] args) {

        int w = 5;
        int l = 4;

        int area = w * l;
        System.out.println(area);


        int perimeter = 2 * l + 2 + w;

        System.out.println(area);
        System.out.println(perimeter);


    }
}
