package day03_Variables;
/*
\n:new line (Enter)
\t: paragraph space(tab)
\" double quote
\\ back slash
//front slash

 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println(" Hello everyone \nHow are you all today/");


        System.out.println(" Hello everyone");
        System.out.println(" My name is Malgorzata");
        System.out.println(" I live in Illinois");


        System.out.println("-------------");
        System.out.println(" Hello Everyone\nMy name is Malgorzata\nI live in Illinois");

        System.out.println("-------------");
        System.out.println("\t Today is a great day to study in Java");

        System.out.println("-------------");
        System.out.println("My favorite Tv series is \"Game of Throne\"");

        System.out.println("-------------");
        System.out.println("/"); // front slash
        System.out.println("\\"); //back slash

    }
}
