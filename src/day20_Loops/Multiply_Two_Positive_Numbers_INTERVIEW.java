package day20_Loops;

public class Multiply_Two_Positive_Numbers_INTERVIEW {
    public static void main(String[] args) {
        int a=5;
        int b=4;
        int result=0;

        for (int i = 0; i<b ; i++) {
           result += a; 
        }
        System.out.println("result = " + result);;
    }
}
