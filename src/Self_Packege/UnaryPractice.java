package Self_Packege;

public class UnaryPractice {
    public static void main(String[] args) {
        //x 2
        //y= x++
        int y = 2;
        System.out.println(y);

        System.out.println("__________________");
        //x=2
        int x = 2;
        System.out.println(x++);

        System.out.println("----------------------");

        //z=2
        int z =2;//1
        System.out.println(--z);
    }
}
