package day28_CustomMethods;

public class Custom_methods_Intro {
    public static void main(String[] args) {

        printHello5Times();
        System.out.println("Margie");
        printHello5Times();
        System.out.println("Wojtek");
        printHello5Times();
    }

    public static void printHello5Times() {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello");
        }

    }

}

